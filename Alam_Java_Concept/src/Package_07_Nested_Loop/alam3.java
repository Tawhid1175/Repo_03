	public static void main(String[] args) {
		//three level Nested loop
         int i , j , k;
		
		for ( i =1; i <=2; i++) {// outer loop 2 times gurbe
			
			for( j = 1; j <=3; j++) {// innner loop 3 time ghurbe
				
				
				for( k = 1; k <=2; k++) {
					
					System.out.println("alam");
				}
			}
			

	}

}
