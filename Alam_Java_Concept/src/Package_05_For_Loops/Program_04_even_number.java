package Package_05_For_Loops;

public class Program_04_even_number {

	public static void main(String[] args) {
		
		// find out the even number
		// 0 the 100 er 
		
		int x;
		
		for ( x  = 0 ; x <= 100; x = x +2) {
			
			System.out.println(" find out even number of 0 to 100 :" + x);
		}
		

	}

}
